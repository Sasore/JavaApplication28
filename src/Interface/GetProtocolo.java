    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        if ((GetAssunto.getText().equals("")) | (GetDescricao.getText().equals("")) | (GetProtocolo.getText().equals(""))) {
            JOptionPane.showMessageDialog(null, "Todos os campos devem ser preenchidos.");//adiciona um label ao lado do JtextField  
        } else {
            Conexao atualiza = new Conexao();
            int retorno = 0;
            try {
                atualiza.atualizaProtocolo(idProtocolo, GetProtocolo.getText(), GetData.getText(), Gethora.getText(), GetAssunto.getText(), GetDescricao.getText());
                if (retorno == 0) {
                    JOptionPane.showMessageDialog(rootPane, "Protocolo alterado com sucesso!");
                    try {
                        listarAssunto(0);
                    } catch (SQLException ex) {
                        Logger.getLogger(GetProtocolo.class.getName()).log(Level.SEVERE, null, ex);
                    }

                }
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(GetProtocolo.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed

