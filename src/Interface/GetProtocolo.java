    public void listarAssunto(int mes) throws ClassNotFoundException, SQLException {
    //que listara os protocolos do banco de dados
        //*******************limpa consultas anteriores
        DefaultTableModel tabela;
        tabela = (DefaultTableModel) jTableAssunto.getModel();
        tabela.setNumRows(0);
        //********************************************
        ArrayList<Protocolo> listaProtocolo;
        Conexao consultasql = new Conexao();//criando um novo objeto de conexao com o BD
        listaProtocolo = consultasql.selectProtocolos(null,mes);//se o mes for igual a zero a busca retornara todos os meses
        for (int i = 0; i < listaProtocolo.size(); i++) {//laço para preencher a tabela de assunto
            DefaultTableModel valor = (DefaultTableModel) jTableAssunto.getModel();
            String id = Integer.toString(listaProtocolo.get(i).getID());
            String tipoprotocolo = Integer.toString(listaProtocolo.get(i).getTipoProtocolo());
            valor.addRow(new String[]{id, tipoprotocolo, listaProtocolo.get(i).getAssunto()});
        }
    }

    public void VerDetalhesProtocolo(int id) throws ClassNotFoundException, SQLException, ParseException {
        //que listara os protocolos do banco de dados
        ArrayList<Protocolo> listaProtocolo;
        Conexao consultasql = new Conexao();//criando um novo objeto de conexao com o BD
        listaProtocolo = consultasql.selectProtocoloID(id);//chamada a função que exibira os detalhes do protocolo
//----------------------------------------------    
//o codigo abaixo altera a mascara do campo getprotocolo para o formato de protocolo usado pela anatel
        if (listaProtocolo.get(0).getTipoProtocolo() == 1) {
            GetProtocolo.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("###.###.####")));
        } else if (listaProtocolo.get(0).getTipoProtocolo() == 0) {
            GetProtocolo.setFormatterFactory(new javax.swing.text.DefaultFormatterFactory(new javax.swing.text.MaskFormatter("####.##.###.###-##")));
        }
        //----------------------------------------------
        if (listaProtocolo.get(0).getTipoProtocolo() == 0) {
            jTextipo.setText("Protocolo Oi");
        }
        if (listaProtocolo.get(0).getTipoProtocolo() == 1) {
            jTextipo.setText("Protocolo Anatel");
        }
        jTextipo.setBackground(Color.white);//exibe o tipo de protocolo -  oi ou Anatel
        GetProtocolo.setText(listaProtocolo.get(0).getProtocolo());
        GetData.setText(listaProtocolo.get(0).getData());
        Gethora.setText(listaProtocolo.get(0).getHora());
        GetAssunto.setText(listaProtocolo.get(0).getAssunto());
        GetDescricao.setText(listaProtocolo.get(0).getDescricao());
    //System.out.println("Tipo de protocolo: "+listaProtocolo.get(0).getTipoProtocolo());
    }

